		<p class="about__paragraph -f-bold">
		ADJACENT is an online journal of emerging media published by the Interactive Telecommunications Program, or ITP. Its mission is to share research, reflection, observations, and opinions from and for the diverse creators that are exploring the possibilities and directions at the frontiers of media and technology.
		</p>
		<p class="about__paragraph">
		Old/New/Next sounds like it could be a slogan of mid-20th century America: casting off the past, ascendantly modern, moving toward a bright future. But in a contemporary moment in which so much of the future feels as though it has already been predicted, data mined, and sold off, this slogan takes on a more ironic tone, and taking the time to cast a critical eye back toward the past seems like an act of resilience, or at least an act of refuge-seeking. The Interactive Telecommunications Program is turning 40 this year, and for Adjacent Issue 6 we wanted to take this birthday as an opportunity for us to look back on the past 40 years of media and technology, reflect on what that history can inform us about the future of the field, and interpret how the dreams of the past brought about the present, for better and for worse.
		</p>
